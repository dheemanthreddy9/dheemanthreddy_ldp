// Variables
let a = 20;
let b = 5;
let c = 10;

// Arithmetic Operators
console.log("Addition:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);
console.log("Modulus:", a % b);
console.log("Exponent:", b ** 2);

// Assignment Operators
let marks = 80;

console.log("\nOriginal Marks:", marks);

marks += 10;
console.log("After += :", marks);

marks -= 5;
console.log("After -= :", marks);

marks *= 2;
console.log("After *= :", marks);

marks /= 5;
console.log("After /= :", marks);

// Comparison Operators
console.log("\nComparison Operators");

console.log(a == c);
console.log(a === c);
console.log(a != b);
console.log(a !== b);
console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);

// Logical Operators
let age = 21;
let hasID = true;

console.log("\nLogical Operators");

console.log(age >= 18 && hasID);
console.log(age < 18 || hasID);
console.log(!hasID);

// Increment & Decrement
let count = 10;

console.log("\nIncrement & Decrement");

count++;
console.log("After ++ :", count);

count--;
console.log("After -- :", count);






